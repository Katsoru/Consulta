public class InstrucaoSwitchCase {
	public static void main(String[] args) {
		int situacao=32;
		
		switch (situacao) {
		case 25:
			System.out.println("Valor 25");
			break;
		case 18:
			System.out.println("Valor 18");
			break;
		case 32:
			System.out.println("Valor 32");
			break;
		default:
			System.out.println("Sem valor");
			break;
		}
	}
}
